public class HelloWorld {

	private String noun;
	private String phrase;

	public HelloWorld(String sNoun){
		this.noun = sNoun;		
	}

	
	public String getNoun(){
		return noun;
	}

	public void setPhrase(String sPhrase){
		this.phrase = sPhrase;
	}
	
	public String getPhrase(){
		return phrase;
	}
	
	

}
